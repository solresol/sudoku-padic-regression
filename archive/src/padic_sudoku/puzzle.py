def get_euler_50() -> list[Puzzle]:
    """Return all 50 Euler puzzles with their solutions."""
    puzzles = []
    for i, (puzzle_str, solution_str) in enumerate(
        zip(EULER_50_PUZZLES, EULER_50_SOLUTIONS, strict=True)
    ):
        puzzle = parse_81_string(puzzle_str, puzzle_id=f"euler_{i+1:02d}")
        solution = parse_81_string(solution_str, puzzle_id=f"euler_{i+1:02d}_sol")
        puzzle.solution = solution.grid
        puzzles.append(puzzle)
    return puzzles


def get_sample_puzzles() -> list[Puzzle]:
    """Return a small sample of puzzles for quick testing."""
    return get_euler_50()[:5]
