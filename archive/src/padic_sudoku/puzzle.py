@dataclass
class Puzzle:
    """A Sudoku puzzle with power-of-2 encoding."""

    grid: list[list[int]]  # 9x9, power-of-2 encoded (0 for unfilled cells)
    clues: set[tuple[int, int]]  # Positions of given cells (immutable during solving)
    solution: list[list[int]] | None  # Known solution if available
    puzzle_id: str  # Identifier for the puzzle

    def copy_grid(self) -> list[list[int]]:
        """Return a deep copy of the grid."""
        return [row[:] for row in self.grid]


def parse_81_string(s: str, puzzle_id: str = "unknown") -> Puzzle:
    """
    Parse an 81-character puzzle string.

    Format: digits 1-9 for givens, '.' or '0' for blanks.
    """
    s = s.strip().replace(".", "0")
    if len(s) != 81:
        raise ValueError(f"Expected 81 characters, got {len(s)}")

    grid = [[0] * 9 for _ in range(9)]
    clues = set()

    for i, ch in enumerate(s):
        r, c = divmod(i, 9)
        if ch != "0":
            d = int(ch)
            grid[r][c] = digit_to_power(d)
            clues.add((r, c))

    return Puzzle(grid=grid, clues=clues, solution=None, puzzle_id=puzzle_id)


def grid_to_string(grid: list[list[int]]) -> str:
    """Convert a grid back to an 81-character string."""
    chars = []
    for row in grid:
        for val in row:
            if val == 0:
                chars.append("0")
            else:
                chars.append(str(power_to_digit(val)))
    return "".join(chars)


